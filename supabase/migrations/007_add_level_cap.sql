-- ============================================================
-- level_cap: 1テーブルで B/C 全セクションを管理
--   B: 11 行 (Lv.2 〜 11/12②)
--   C:  9 行 (Lv.5 〜 13)
--   合計: 20 行
-- ============================================================

CREATE TABLE level_cap (
  id                   serial  PRIMARY KEY,
  belt_type            text    NOT NULL,
  level                text    NOT NULL,
  sort_order           integer NOT NULL,
  UNIQUE (belt_type, level),
  -- レベルキャップ処理
  min_exp              text    NOT NULL DEFAULT '',
  min_growth           text    NOT NULL DEFAULT '',
  min_reward           text    NOT NULL DEFAULT '',
  min_honor            text    NOT NULL DEFAULT '',
  max_adventurer_rank  text    NOT NULL DEFAULT '',
  -- GM報酬
  reward_amount        text    NOT NULL DEFAULT '',
  off_balance_reward   text    NOT NULL DEFAULT '',
  honor                text    NOT NULL DEFAULT '',
  -- セッション報酬
  distribution         text    NOT NULL DEFAULT '',
  reward_per_session   text    NOT NULL DEFAULT '',
  sword_fragments      text    NOT NULL DEFAULT '',
  growth_count         text    NOT NULL DEFAULT '',
  growth_limit         text    NOT NULL DEFAULT '',
  exp_limit            text    NOT NULL DEFAULT '',
  f_count_limit        text    NOT NULL DEFAULT '',
  reward_limit         text    NOT NULL DEFAULT '',
  excess_growth        text    NOT NULL DEFAULT ''
);

ALTER TABLE level_cap ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Public read" ON level_cap FOR SELECT USING (true);

